    public void TablaHospital(){
        try {
            preparedStatement = conexion.prepareStatement("select * from hospitales");
            resultset = preparedStatement.executeQuery();
            java.sql.ResultSetMetaData Rsm = resultset.getMetaData();
            int conteoColumna;
            conteoColumna = Rsm.getColumnCount();
            DefaultTableModel defaultTable = (DefaultTableModel)tablaHospital.getModel();
            defaultTable.setRowCount(0);
            
            while(resultset.next()){
                Vector vector = new Vector();
                
                for (int indiceColumna = 0; indiceColumna < conteoColumna; indiceColumna++) {
                    vector.add(resultset.getString("id_hospital"));
                    vector.add(resultset.getString("nombre_hospital"));
                    vector.add(resultset.getString("provincia"));
                    vector.add(resultset.getString("canton"));
                    vector.add(resultset.getString("distrito"));
                    vector.add(resultset.getString("direccion"));
                    vector.add(resultset.getString("pisos"));
                    vector.add(resultset.getString("consultorios"));
                    vector.add(resultset.getString("año_construccion"));
                }
                defaultTable.addRow(vector);
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Hospital.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    

