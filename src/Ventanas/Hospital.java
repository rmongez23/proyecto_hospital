    private void agregarHospitalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarHospitalButtonActionPerformed
        
        String nombreHospital = nombreHospitalText.getText();
        String provinciaHospital = provinciaHospitalText.getText();
        String cantonHospital = cantonHospitalText.getText();
        String distritoHospital = distritoHospitalText.getText();
        int pisosHospital = Integer.parseInt(pisosHospitalText.getText());
        String direccionHospital = direccionHospitalText.getText();
        String consultoriosHospital = consultoriosHospitalText.getText();
        String añoHospital = añoHospitalText.getText();
        Conexion();
        try {
            preparedStatement = conexion.prepareStatement("insert into hospitales(nombre_hospital,provincia,canton,distrito,direccion,pisos,consultorios,año_construccion)values(?,?,?,?,?,?,?,?)");
            preparedStatement.setString(1,nombreHospital);
            preparedStatement.setString(2,provinciaHospital);
            preparedStatement.setString(3,cantonHospital); 
            preparedStatement.setString(4,distritoHospital);
            preparedStatement.setString(5,direccionHospital);
            preparedStatement.setInt(6,pisosHospital);
            preparedStatement.setString(7,consultoriosHospital);
            preparedStatement.setString(8,añoHospital);
            preparedStatement.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Hospital ingresado");
            
            nombreHospitalText.setText("");
            provinciaHospitalText.setText("");
            cantonHospitalText.setText("");
            distritoHospitalText.setText("");
            pisosHospitalText.setText("");
            direccionHospitalText.setText("");
            consultoriosHospitalText.setText("");
            añoHospitalText.setText("");
            nombreHospitalText.requestFocus();
            TablaHospital();
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_agregarHospitalButtonActionPerformed

