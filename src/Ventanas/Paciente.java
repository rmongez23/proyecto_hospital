    private void agregarPacienteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarPacienteButtonActionPerformed
        int cedulaPaciente = Integer.parseInt(cedulaPacienteText.getText());
        String nombrePaciente = nombrePacienteText.getText();
        String apellido1Paciente = apellido1PacienteText.getText();
        String apellido2Paciente = apellido2PacienteText.getText();
        int telefonoPaciente = Integer.parseInt(telefonoPacienteText.getText());
        String estadoCivilPaciente = estadoCivilPacienteCombobox.getSelectedItem().toString();
        String correoPaciente = correoPacienteText.getText();
        String estadoPaciente = estadoPacienteCombobox.getSelectedItem().toString();
        
        Conexion();
        try {
            preparedStatement = conexion.prepareStatement("insert into pacientes(cedula,primer_apellido,segundo_apellido,nombre,telefono,estado_civil,correo,estado_paciente)values(?,?,?,?,?,?,?,?)");
            preparedStatement.setInt(1,cedulaPaciente);
            preparedStatement.setString(2, apellido1Paciente);
            preparedStatement.setString(3, apellido2Paciente); 
            preparedStatement.setString(4,nombrePaciente);
            preparedStatement.setInt(5, telefonoPaciente);
            preparedStatement.setString(6, estadoCivilPaciente);
            preparedStatement.setString(7, correoPaciente);
            preparedStatement.setString(8, estadoPaciente);
            preparedStatement.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Paciente ingresado");
            
            cedulaPacienteText.setText("");
            nombrePacienteText.setText("");
            apellido1PacienteText.setText("");
            apellido2PacienteText.setText("");
            estadoCivilPacienteCombobox.setSelectedIndex(-1);
            telefonoPacienteText.setText("");
            estadoPacienteCombobox.setSelectedIndex(-1);
            correoPacienteText.setText("");
            cedulaPacienteText.requestFocus();
            TablaPaciente();
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_agregarPacienteButtonActionPerformed

